# --- 1. Продукттар менен бааларды киргизүү ---
products = {}

while True:
    name = input("Продукттун атын жазыңыз (же 'стоп' деп токтотуңуз): ")
    if name.lower() == "стоп":
        break
    price = float(input(f"'{name}' продуктынын баасын жазыңыз: "))
    products[name] = price

print("\nСакталган продукттар жана алардын баалары:")
print(products)

# --- 2. Продукттун баасын издөө ---
search = input("\nБаасын билгиңиз келген продукттун атын жазыңыз: ")
if search in products:
    print(f"'{search}' продуктынын баасы: {products[search]} сом.")
else:
    print("Мындай продукт сөздүктө жок.")

# --- 3. Белгилүү суммадан арзаныраак продукттарды чыгаруу ---
limit = float(input("\nСумманы жазыңыз (мисалы: 50): "))
print(f"{limit} сомдон арзаныраак болгон продукттар:")
found = False
for item, price in products.items():
    if price < limit:
        print(f" - {item}: {price} сом")
        found = True
if not found:
    print("Мындай продукт табылган жок.")

# --- 4. Айлардын жана күндөрдүн сөздүгү ---
months = {
    "Январь": 31,
    "Февраль": 28,
    "Март": 31,
    "Апрель": 30,
    "Май": 31,
    "Июнь": 30,
    "Июль": 31,
    "Август": 31,
    "Сентябрь": 30,
    "Октябрь": 31,
    "Ноябрь": 30,
    "Декабрь": 31
}

# --- 5. Колдонуучу айдын атын жазат, программа күндөрдү чыгарат ---
month = input("\nАйдын атын жазыңыз (мисалы: Март): ")
if month in months:
    print(f"{month} айында {months[month]} күн бар.")
else:
    print("Мындай ай жок.")

# --- 6. Бардык айларды алфавит боюнча чыгаруу ---
print("\nАйлар алфавит боюнча:")
for m in sorted(months.keys()):
    print(m)

# --- 7. 31 күн болгон айларды чыгаруу ---
print("\n31 күн болгон айлар:")
for m, days in months.items():
    if days == 31:
        print(m)

# --- 8. Айларды күндөрдүн саны боюнча сорттоо жана чыгаруу ---
print("\nАйлар жана алардын күндөрү (күндөрдүн саны боюнча иреттелген):")
for m, d in sorted(months.items(), key=lambda x: x[1]):
    print(f"{m} — {d} күн")
